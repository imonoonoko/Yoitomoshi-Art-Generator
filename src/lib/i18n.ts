export const TRANSLATIONS: Record<UiLanguage, Dict> = { ja, en, ru, pt }

function format(template: string, params?: Record<string, string | number>): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_m, k) =>
    params[k] !== undefined ? String(params[k]) : `{${k}}`
  )
}

/** Resolve a translation key in the active language with parameter substitution. */
function resolve(lang: UiLanguage, key: string, params?: Record<string, string | number>): string {
  const dict = TRANSLATIONS[lang] ?? TRANSLATIONS.ja
  // Fallback chain: requested lang -> en -> ja -> raw key.
  // English is the safest partial-localization fallback because it avoids
  // Japanese text leaking into the Russian/Portuguese UI while missing keys
  // are being translated.
  const template = dict[key] ?? TRANSLATIONS.en[key] ?? TRANSLATIONS.ja[key] ?? key
  return format(template, params)
}

/**
 * React hook returning a `t(key, params?)` function. Re-renders the calling
 * component whenever the user changes language in settings.
 */
export function useT(): (key: string, params?: Record<string, string | number>) => string {
  const lang = useStore((s) => s.settings?.uiLanguage ?? 'ja')
  return (key, params) => resolve(lang, key, params)
}

/**
 * Non-hook translation lookup, for use in event handlers, IPC callbacks, or
 * other contexts where you can't call hooks. Reads the language from the
 * store directly — does NOT trigger a re-render on language change, so
 * any string built with this is captured at call time.
 */
export function t(key: string, params?: Record<string, string | number>): string {
  const lang = useStore.getState().settings?.uiLanguage ?? 'ja'
  return resolve(lang, key, params)
}
