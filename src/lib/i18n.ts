/**
 * Lightweight in-house i18n.
 *
 * Why not react-i18next: this app is small and ships its own translation set
 * — adding a 200KB dependency for `t(key)` + a parameter substitution pass is
 * overkill. The hook re-renders when `settings.uiLanguage` changes (it reads
 * via the Zustand selector), so consumers don't have to think about subscriptions.
 *
 * Adding a string:
 *   1. Pick a stable key like `settings.forgePath`
 *   2. Add an entry to ALL four dictionaries (missing entries fall back to en, then ja)
 *   3. Replace the JSX literal with `t('settings.forgePath')`
 *
 * Parameters use `{name}` placeholder syntax: `t('toast.imported', { count: 3 })`.
 */

